package secondweek.sequencegenerator;

/**
 * Represents a SequenceGenerator.
 *
 * @author Ylab
 * @version 1.0
 */
public interface SequenceGenerator {
    /**
     * Outputs to the console n first members of A sequence
     *
     * @param n members of this sequence for output
     */
    void a(int n);

    /**
     * Outputs to the console n first members of B sequence
     *
     * @param n members of this sequence for output
     */
    void b(int n);

    /**
     * Outputs to the console n first members of C sequence
     *
     * @param n members of this sequence for output
     */
    void c(int n);

    /**
     * Outputs to the console n first members of D sequence
     *
     * @param n members of this sequence for output
     */
    void d(int n);

    /**
     * Outputs to the console n first members of E sequence
     *
     * @param n members of this sequence for output
     */
    void e(int n);

    /**
     * Outputs to the console n first members of F sequence
     *
     * @param n members of this sequence for output
     */
    void f(int n);

    /**
     * Outputs to the console n first members of G sequence
     *
     * @param n members of this sequence for output
     */
    void g(int n);

    /**
     * Outputs to the console n first members of H sequence
     *
     * @param n members of this sequence for output
     */
    void h(int n);

    /**
     * Outputs to the console n first members of I sequence
     *
     * @param n members of this sequence for output
     */
    void i(int n);

    /**
     * Outputs to the console n first members of J sequence
     *
     * @param n members of this sequence for output
     */
    void j(int n);
}
